    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        if(Name.getText().equals("") || DateTime.getDateTimeStrict() == null){
            Error.setText("*All fields are required");
        }
        else{
            String name= Name.getText();
            String department= Department.getSelectedItem().toString();
            String dateTime = DateTime.getDateTimeStrict().toString();
            apm.Add("xyz", name, department, dateTime);
        }
        JOptionPane.showMessageDialog(this, "APPOINTMENT CONFIRMED");
        Department.setSelectedItem("OPD");
        Name.setText("");
        DateTime.clear();
        DefaultTableModel tblModel = (DefaultTableModel)appointment_table.getModel();
        tblModel.setRowCount(0);
        displayAppointment(apm.getAppointmentsList());
    }//GEN-LAST:event_jButton2MouseClicked

