    public void displayAppointment(List<Appointment> appointments){
        for(Appointment apm:appointments){
            int queue = apm.queueGetter();
            String name = apm.NameGetter();
            String department = apm.DepartmentGetter();
            String[] dateTime = apm.dateTimeGetter().split("T");
            String newdata[]={String.valueOf(queue),name,department,dateTime[0],dateTime[1]};
            DefaultTableModel tblModel = (DefaultTableModel)appointment_table.getModel();
            tblModel.addRow(newdata);
        }
    }
